package calcular;

public class Masa {
    private double altura=0;
    private double peso=0;

    public Masa() {
    }

    public double getAltura(){
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso){
        this.peso = peso;
    }
}
